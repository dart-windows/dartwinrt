/// Indicates that the class is an activatable runtime class.
const activatableAttribute = 'Windows.Foundation.Metadata.ActivatableAttribute';

/// Indicates that a type or member should be marked in metadata as deprecated.
const deprecatedAttribute = 'Windows.Foundation.Metadata.DeprecatedAttribute';

/// Indicates that the specified type is exclusive to this type.
const exclusiveToAttribute = 'Windows.Foundation.Metadata.ExclusiveToAttribute';

/// Indicates that an enumeration can be treated as a bit field; that is, a set
/// of flags.
const flagsAttribute = 'System.FlagsAttribute';

/// Identifies the method as an overload in a language that supports
/// overloading.
const overloadAttribute = 'Windows.Foundation.Metadata.OverloadAttribute';

/// Indicates an interface that contains only static methods.
const staticAttribute = 'Windows.Foundation.Metadata.StaticAttribute';
